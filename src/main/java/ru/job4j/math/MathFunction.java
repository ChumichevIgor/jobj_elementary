package ru.job4j.math;

public class MathFunction {
    public static double sum(double first, double second) {
        return first + second;
    }

    public static double multiply(double first, double second) {
        return first * second;
    }

    public static double dif(double third, double fourth) {
        return third - fourth;
    }

    public static double div(double third, double fourth) {
        return third / fourth;
    }
}
